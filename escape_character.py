s = 'hello\ni am shivmohan sharma' 
print(s)
# hello
# i am shivmohan sharma

s = 'hello\ti am shivmohan sharma' 
print(s)  # hello   i am shivmohan sharma

s = 'hello\bi am shivmohan sharma' 
print(s)  # helli am shivmohan sharma


s = 'hello i am \'shivmohan sharma\'' 
print(s)  # hello i am 'shivmohan sharma'


s = 'hello i am \"shivmohan sharma\"' 
print(s)  # hello i am "shivmohan sharma"

s = 'hello\\hi i am \"shivmohan sharma\"' 
print(s)  # hello\hi i am "shivmohan sharma"